public class ArithmeticOperations {

	// Write a static method that adds num1 to num2
    public static int add (int x, int y){
        int sum = x + y;
        return sum;
    }
	// Write a static method that subtracts num2 from num1
    public static int subtract(int x, int y){
        int difference = x - y;
        return difference;
    }

	// Write a static method that multiplies num1 by num2
    public static double multiply (double x, double y){
        double product = x * y;
        return product;
    }
	// Write a static method that divides the numerator by the denominator
    public static int divide(int x, int y){
        int quotient = x / y;
        return  quotient;
    }
}
